char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while (src[i] != '\0' && i < n)
	{
		dest[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}

#include <stdio.h>

int main (void)
{
	char memoria[21];
	char name[] = "Beah 123";
	
	ft_strncpy(memoria, name,7);
	printf("%s", memoria);
	
	return (0);	
}